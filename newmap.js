let storesSeattle = document.getElementById('stores-seattle');
let storesEast = document.getElementById('stores-east');
let storesNorth = document.getElementById('stores-north');
let storesSouth = document.getElementById('stores-south');

retailers.forEach(function(e){
    let storeDiv = document.createElement('div');
    storeDiv.classList.add("store");
    let storeImg = document.createElement('img');
    let storeAdress = document.createElement('p');
    let storeCity = document.createElement('p');
    storeImg.src = e.logo;
    storeAdress.textContent = e.address;
    storeCity.textContent = e.city;
    storeDiv.append(storeImg,storeAdress,storeCity);

    if(e.group === "seattle"){
        storesSeattle.append(storeDiv);
    }else if(e.group === "east"){
        storesEast.append(storeDiv);
    }else if(e.group === "north"){
        storesNorth.append(storeDiv);
    }else{
        storesSouth.append(storeDiv);
    }
});
